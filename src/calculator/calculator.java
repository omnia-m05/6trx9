    private void operator_actions(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_operator_actions
        // TODO add your handling code here:
        if (edit_text.getText().length() > 0) {
            
            switch (evt.getActionCommand()) {
                
                case "+":
                    number1 = Double.parseDouble(edit_text.getText());
                    operator = '+';
                    label_show.setText(number1 + "+");
                    edit_text.setText("");
                    break;
                
                case "×":
                    number1 = Double.parseDouble(edit_text.getText());
                    operator = '*';
                    label_show.setText(number1 + "×");
                    edit_text.setText("");
                    break;
                
                case "÷":
                    number1 = Double.parseDouble(edit_text.getText());
                    operator = '/';
                    label_show.setText(number1 + "÷");
                    edit_text.setText("");
                    break;
                
                case "-":
                    number1 = Double.parseDouble(edit_text.getText());
                    operator = '-';
                    label_show.setText(number1 + "-");
                    edit_text.setText("");
                    break;
            }
        }

    }//GEN-LAST:event_operator_actions


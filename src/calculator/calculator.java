    private void button_equalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_equalActionPerformed
        // TODO add your handling code here:
        if (operator != '\u0000') {
            if (edit_text.getText().length() > 0) {
                number2 = Double.parseDouble(edit_text.getText());
            } else {
                number2 = number1;
            }
            
            double result;
            
            switch (operator) {
                
                case '+':
                    result = number1 + number2;
                    edit_text.setText("" + result);
                    // edit_text.setText(String.valueOf(result));
                    label_show.setText(label_show.getText() + number2 + "=");
                    break;
                
                case '/':
                    result = number1 / number2;
                    edit_text.setText("" + result);
                    // edit_text.setText(String.valueOf(result));
                    label_show.setText(label_show.getText() + number2 + "=");
                    break;
                
                case '-':
                    result = number1 - number2;
                    edit_text.setText("" + result);
                    // edit_text.setText(String.valueOf(result));
                    label_show.setText(label_show.getText() + number2 + "=");
                    break;
                
                case '*':
                    result = number1 * number2;
                    edit_text.setText("" + result);
                    // edit_text.setText(String.valueOf(result));
                    label_show.setText(label_show.getText() + number2 + "=");
                    break;
                
            }
            operator = '\u0000';
        } else {
            edit_text.setText(number2 + "");
        }

    }//GEN-LAST:event_button_equalActionPerformed

